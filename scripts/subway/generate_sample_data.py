def generate_stations():
    """역 데이터 생성"""
    stations = []
    for s in STATIONS_DATA:
        station_id = f"line{s['line']}_{s['name']}"
        is_transfer = s["name"].replace("역", "") in TRANSFER_STATIONS or s["name"] in TRANSFER_STATIONS
        stations.append({
            "id": station_id,
            "name": s["name"],
            "line": s["line"],
            "lat": s["lat"],
            "lng": s["lng"],
            "is_transfer": is_transfer
        })
    return stations


def generate_edges():
    """역간 엣지 생성"""
    edges = []

    for line, sequence in LINE_SEQUENCES.items():
        for i in range(len(sequence) - 1):
            from_name = sequence[i]
            to_name = sequence[i + 1]

            # 순환선 처리 (2호선)
            if from_name == to_name:
                continue

            from_id = f"line{line}_{from_name}"
            to_id = f"line{line}_{to_name}"

            # 평균 역간 시간 2분 (실제는 1.5~3분)
            edges.append({
                "from": from_id,
                "to": to_id,
                "line": line,
                "minutes": 2,
                "is_transfer": False
            })

    return edges


def generate_transfers():
    """환승 엣지 생성"""
    transfers = []

    for station_name, transfer_list in TRANSFER_STATIONS.items():
        for from_line, to_line, minutes in transfer_list:
            # 역명에 "역" 붙이기
            name = station_name if station_name.endswith("역") else station_name + "역"

            from_id = f"line{from_line}_{name}"
            to_id = f"line{to_line}_{name}"

            transfers.append({
                "station": name,
                "from": from_id,
                "to": to_id,
                "from_line": from_line,
                "to_line": to_line,
                "minutes": minutes
            })

            # 양방향
            transfers.append({
                "station": name,
                "from": to_id,
                "to": from_id,
                "from_line": to_line,
                "to_line": from_line,
                "minutes": minutes
            })

    return transfers


